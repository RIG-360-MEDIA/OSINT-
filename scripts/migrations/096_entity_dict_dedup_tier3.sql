-- Flatten redirect chains: A->B->C should make A point straight to C.
-- Tier 3 redirects can create transitive chains where a Tier 3 winner is itself a Tier 1/2
-- loser. Without flattening, entity_lookup ends up pointing to redirected rows (32 such
-- pointers were observed when this was first applied). Recursive CTE collapses each chain to
-- its terminal (non-redirected) ancestor; safe to re-run (no-op when no chains exist).
WITH RECURSIVE chain AS (
  SELECT id, redirected_to AS target, 1 AS hop FROM public.entity_dictionary
   WHERE redirected_to IS NOT NULL
  UNION ALL
  SELECT c.id, ed.redirected_to, c.hop + 1 FROM chain c
   JOIN public.entity_dictionary ed ON ed.id = c.target
   WHERE ed.redirected_to IS NOT NULL AND c.hop < 10
),
terminal AS (
  SELECT DISTINCT ON (id) id, target FROM chain ORDER BY id, hop DESC
)
UPDATE public.entity_dictionary t SET redirected_to = terminal.target
FROM terminal WHERE t.id = terminal.id AND t.redirected_to <> terminal.target;

-- Re-flip entity_lookup pointers against the now-flattened dictionary
UPDATE public.entity_lookup el SET entity_id = ed.redirected_to
FROM public.entity_dictionary ed
WHERE el.entity_id = ed.id AND ed.redirected_to IS NOT NULL;

