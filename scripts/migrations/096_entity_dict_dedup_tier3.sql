  SELECT array(SELECT DISTINCT x FROM unnest(coalesce(b.aliases,ARRAY[]::text[]) ||
    (SELECT array_agg(t.canonical_name) FROM public.entity_dictionary t WHERE t.redirected_to=b.id)) x WHERE x IS NOT NULL AND x<>'')
